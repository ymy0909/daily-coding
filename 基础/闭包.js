// 什么是作用域？
// 作用域就是变量可访问的管理范围（或者说变量的管理范围）。
// 举个例子：班级里要准备大扫除了，班长给班级的值日生安排打扫任务。小明同学负责打扫厕所，小红和小丁同学负责打扫教室，
// 小龙同学负责操场的卫生。那么我们可以说小明的管理范围就是厕所区域，小龙同学的管理范围是操场区域；
// 换个说法就是小明的作用域是厕所，小龙的作用域是操场。
// 一般作用域可以分为词法作用域（也叫静态作用域）和动态作用域，而js中一般是词法作用域。